# ცვლადების ინიციალიზაცია
x = 10
y = 5

# 1. მიმატება
x += y  # x = x + y
print("მიმატების შედეგი:", x)

# 2. გამოკლება
x -= 3  # x = x - 3
print("გამოკლების შედეგი:", x)

# 3. გამრავლება
x *= 2  # x = x * 2
print("გამრავლების შედეგი:", x)

# 4. გაყოფა
x /= 4  # x = x / 4
print("გაყოფის შედეგი:", x)

# 5. მთელი გაყოფა (დასასრულავად)
x //= 2  # x = x // 2
print("მთლიანი გაყოფის შედეგი:", x)