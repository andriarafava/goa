# და (and) ოპერატორი
print(True and True)   # True: ორივე პირობა მართალია
print(True and False)  # False: ერთი პირობა მცდარია
print(False and True)  # False: ერთი პირობა მცდარია
print(False and False) # False: ორივე პირობა მცდარია

# ან (or) ოპერატორი
print(True or True)    # True: ერთი მაინც პირობა მართალია
print(True or False)   # True: ერთი მაინც პირობა მართალია
print(False or True)   # True: ერთი მაინც პირობა მართალია
print(False or False)  # False: ორივე პირობა მცდარია
